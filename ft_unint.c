#include "ft_printf.h"

int	ft_unint(unsigned int n)
{
	int	x;

	x = 0;
	if (n >= 0 && n <= 9)
	{
		n += '0';
		x += ft_puchar(n);
	}
	else
	{
		x += ft_unint(n / 10);
		x += ft_unint(n % 10);
	}
	return (x);
}
