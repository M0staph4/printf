#include "ft_printf.h"

int	ft_lwhx(unsigned int c)
{
	int		x;
	char	*p;

	x = 0;
	p = "0123456789abcdef";
	if (c < 16)
		x += ft_puchar(p[c]);
	else if (c >= 16)
	{
		x += ft_lwhx(c / 16);
		x += ft_lwhx(c % 16);
	}
	return (x);
}
